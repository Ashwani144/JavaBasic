package package3;

public class SwapNumber
{

	public static void main(String[] args)
	{
		
       int a=10;
       int b=20;
       int c;
 
       System.out.println("Before swap Number");
       System.out.println("First Number :"+ a);
       System.out.println("second Number :"+ b);
       
       c=a;
       a=b;
       b=c;
       
       System.out.println("After swap Number");
       System.out.println("First Number :"+ a);
       System.out.println("second Number :"+ b);
		

	}

}
